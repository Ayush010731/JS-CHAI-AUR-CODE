// while loop to iterate from 0 to 4 and log the iteration number
let count = 0;
while (count < 5) {
    console.log('Iteration number:', count);
    count++;
}
// do...while loop to iterate from 0 to 4 and log the iteration number
let num = 0;
do {
    console.log('Iteration number:', num);
    num++;
} while (num < 5);
console.log('Done with iterations!');
